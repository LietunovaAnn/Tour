CREATE TABLE "DISCOUNT"
(
    "DISCOUNT_ID"          NUMBER NOT NULL,
    "PARTICIPATION_NUMBER" NUMBER NOT NULL,
    "PERCENT"              NUMBER NOT NULL,
    constraint "DISCOUNT_PK" PRIMARY KEY ("DISCOUNT_ID")
);


CREATE TABLE "CUSTOMERS"
(
    "CUSTOMERS_ID"         NUMBER       NOT NULL,
    "CUSTOMERS_NAME"       VARCHAR(100) NOT NULL,
    "CUSTOMERS_EMAIL"      VARCHAR(100) NOT NULL,
    "PARTICIPATION_NUMBER" NUMBER,
    constraint "CUSTOMERS_PK" PRIMARY KEY ("CUSTOMERS_ID")
);


CREATE TABLE "COMPLEXITY"
(
    "COMPLEXITY_ID"   NUMBER       NOT NULL,
    "COMPLEXITY_NAME" VARCHAR(100) NOT NULL,
    constraint "COMPLEXITY_PK" PRIMARY KEY ("COMPLEXITY_ID")
);


CREATE TABLE "TYPE_OF_TOUR"
(
    "TYPE_OF_TOUR_ID"   NUMBER       NOT NULL,
    "TYPE_OF_TOUR_NAME" VARCHAR(100) NOT NULL,
    constraint "TYPE_OF_TOUR_PK" PRIMARY KEY ("TYPE_OF_TOUR_ID")
);


CREATE TABLE "TOUR"
(
    "TOUR_ID"       NUMBER       NOT NULL,
    "TOUR_NAME"     VARCHAR(100) NOT NULL,
    "TOUR_PRICE"    NUMBER       NOT NULL,
    "COMPLEXITY_ID" NUMBER       NOT NULL,
    constraint "TOUR_PK" PRIMARY KEY ("TOUR_ID"),
    CONSTRAINT "FK_COMPLEXITY"
        FOREIGN KEY ("COMPLEXITY_ID")
            REFERENCES "COMPLEXITY" ("COMPLEXITY_ID")
);


CREATE TABLE "VARIATION"
(
    "TOUR_ID"         NUMBER,
    "TYPE_OF_TOUR_ID" NUMBER,
    CONSTRAINT "FK_TOUR"
        FOREIGN KEY ("TOUR_ID")
            REFERENCES "TOUR" ("TOUR_ID"),
    CONSTRAINT "FK_TYPE_OF_TOUR"
        FOREIGN KEY ("TYPE_OF_TOUR_ID")
            REFERENCES "TYPE_OF_TOUR" ("TYPE_OF_TOUR_ID")
);


CREATE TABLE "ORDERS"
(
    "ORDERS_ID"      NUMBER NOT NULL,
    "TOUR_ID"        NUMBER NOT NULL,
    "CUSTOMERS_ID"   NUMBER NOT NULL,
    "DISCOUNT_PRICE" NUMBER NOT NULL,
    CONSTRAINT "ORDERS_PK" PRIMARY KEY ("ORDERS_ID"),
    CONSTRAINT "FK0_TOUR"
        FOREIGN KEY ("TOUR_ID")
            REFERENCES "TOUR" ("TOUR_ID"),
    CONSTRAINT "FK_CUSTOMERS"
        FOREIGN KEY ("CUSTOMERS_ID")
            REFERENCES "CUSTOMERS" ("CUSTOMERS_ID")
);

CREATE SEQUENCE "ORDERS_SEQ";

CREATE SEQUENCE "TOUR_SEQ";

CREATE SEQUENCE "TYPE_OF_TOUR_SEQ";

CREATE SEQUENCE "DISCOUNT_SEQ";

CREATE SEQUENCE "CUSTOMERS_SEQ";

CREATE SEQUENCE "COMPLEXITY_SEQ";

